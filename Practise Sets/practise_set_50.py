def countApplesAndOranges(s, t, a, b, apples, oranges):
    # Write your code here
    apples_cross = 0
    oranges_cross = 0

    for _ in apples:
        if a + _ >= s:
            apples_cross += 1
    
    for _ in oranges:
        if b + _ <= b:
            oranges_cross += 1
    
    print(apples_cross, oranges_cross, sep="\n")   


