# Complete the 'viralAdvertising' function below.

# The function is expected to return an INTEGER.
# The function accepts INTEGER n as parameter.


def viralAdvertising(n: int) -> int:
    shared = [2]
    for i in range(n - 1):
        shared.append(int(3 * shared[i] / 2))

    return sum(shared)


